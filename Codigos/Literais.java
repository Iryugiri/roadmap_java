public class Literais {

    public static void main(String[] args) {

        int hex = 0xFF;
        int oct = 0177;

        System.out.println("Esse é o valor 0xFF em hexadecimal: " + hex);
        System.out.println("Esse é o valor 0177 em octal: " + oct);

        int bin = 0b1100;
        System.out.println("Esse é o valor 1100 em binario: " + bin);

    }
}
